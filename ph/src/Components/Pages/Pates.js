const main = document.querySelector('main');

const Pate = () => {
  main.innerHTML = `
    <link rel="preconnect" href="https://fonts.googleapis.com">
    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
    <link href="https://fonts.googleapis.com/css2?family=Roboto+Condensed&family=Roboto:wght@300&display=swap" rel="stylesheet">
    <link rel="stylesheet" href="https://fonts.googleapis.com/css2?family=Material+Symbols+Outlined:opsz,wght,FILL,GRAD@48,400,0,0" />

    <div class="menu-container">
  <div class="menu-content">
    <label class="labelAffichage">
      <input type="radio" name="category" value="ravier" checked> Ravier
    </label>
    <label class="labelAffichage">
      <input type="radio" name="category" value="assiete"> Assiette
    </label>
    <label class="labelAffichage">
      <input type="radio" name="category" value="gratnie"> Gratiné
    </label>
  </div>
</div>
<main class="lacard"></main>`;

  // Ajouter un écouteur d'événements aux radios
  document.querySelectorAll('input[name="category"]').forEach(radio => {
    radio.addEventListener('change', () => {
      // Appeler la fonction pour mettre à jour les pâtes avec la catégorie sélectionnée
      updatePates(radio.value);
    });
  });

  // Ajouter un écouteur d'événements aux cartes
  main.querySelector('.lacard').addEventListener('click', (event) => {
    const clickedCard = event.target.closest('.card');
    if (clickedCard) {
      // Désactiver toutes les autres cartes
      document.querySelectorAll('.card').forEach(card => {
        if (card !== clickedCard) {
          card.classList.remove('active');
        }
      });

      // Ajouter ou supprimer la classe 'active' pour le clic sur la carte
      clickedCard.classList.toggle('active');
    }
  });

  // Afficher les pâtes de la catégorie "ravier" par défaut
  updatePates('ravier');
};

const renderPate = (pate) => `
  <div class="card">
    <img src="${pate.imgSrc}" alt="${pate.title}">
    <div class="card-content">
      <h2 class="hcard">${pate.title}</h2>
      <p class="pcard">${pate.text}</p>
    </div>
  </div>
`;

const updatePates = (category) => {
  // Filtrer les pâtes en fonction de la catégorie sélectionnée
  const filteredPates = category === 'all' ? dataPate : dataPate.filter(pate => pate.category === category);

  // Construire le HTML pour afficher les pâtes
  const patesHTML = filteredPates.map(renderPate).join('');

  // Mettre à jour le contenu principal avec les pâtes
  main.querySelector('.lacard').innerHTML = patesHTML;
};

// Appeler la fonction pour initialiser le contenu HTML
Pate();

// Exporter la constante Pate
export default Pate;